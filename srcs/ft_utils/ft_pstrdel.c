#include "../../includes/libft.h"

void	ft_pstrdel(char **pstr)
{
	int		i;

	i = 0;
	while (pstr[i] != NULL)
	{
		ft_strdel(&pstr[i]);
		i++;
	}
	free(pstr);
}
